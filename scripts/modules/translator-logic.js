const morseArr = Object.entries(morseCode);

export const translate = (value) => {
  let newArr = [];
  let translation = "";
  for (let x of value) {
    newArr.push(
      morseArr.map((char) => (x.toUpperCase() === char[0] ? char[1] : ""))
    );
  }
  return (translation = newArr.flat().join(""));
};
